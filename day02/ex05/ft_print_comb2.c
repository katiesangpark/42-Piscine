int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while (i <= 98)
	{
		ft_putchar('0' + (i / 10));
		ft_putchar('0' + (i % 10));
		ft_putchar(' ');
		ft_putchar('0' + (j / 10));
		ft_putchar('0' + (j % 10));
		j = j + 1;
		if (!(i == 98))
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
		if (j == 100)
		{
			i = i + 1;
			j = i + 1;
		}
	}
}
