int		ft_iterative_factorial(int nb)
{
	int i;

	if (nb >= 13)
	{
		return (0);
	}
	i = nb;
	nb = nb - 1;
	while (nb > 1)
	{
		i = i * nb;
		nb = nb - 1;
	}
	return (i);
}
